# Non-primitive types:
# They are different from primitive types in that
# they are not directly supported by the programming language
# but are instead implemented using its basic data types.

# Non-primitive types in python:
# - list
# - tuple
# - set
# - dictionaty
# - lots of other...

# list: sequence of items
names = ["mahdi", "ali"]

# tuple: sequence of items
scores = (1, 2, 3)

# set: bag of uniqe items
prime_numbers = {2, 3, 5, 7}

# dictionaty: key value pairs
person = {
    "name": "mahdi",
    "age": 21
}
