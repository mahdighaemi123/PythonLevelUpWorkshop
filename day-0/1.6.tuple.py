# Tuples:
# Similar to lists, but with the key difference that they are immutable,
# meaning once they are created, their elements cannot be modified, added, or removed.

# create empty tupe
t = tuple()

# create tupe with value
t = (1, 2, 3, 1)
t = (1,)

# dont create tuples like this
number = (1)  # this is not a tuple its a number
